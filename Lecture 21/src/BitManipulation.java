public class BitManipulation {
    public static void main(String[] args){
        // binary AND '&'
        System.out.println(5 & 6);
        // binary OR '|'
        System.out.println(5 | 6);
        // binary XOR '^'
        System.out.println(5 ^ 6);
        //1's complement
        System.out.println(~0);
        // binary left shift '<<'
        System.out.println(5 << 2);
        System.out.println(6 >> 1);

    }
}
